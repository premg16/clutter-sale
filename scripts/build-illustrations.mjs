/**
 * Generate `src/app/v4/Illustrations.tsx` — the four illustrations as INLINE
 * React components.
 *
 * Run: node scripts/build-illustrations.mjs
 *
 * Why inline rather than <Image src="...svg">:
 *   An <img> renders its SVG in an isolated document, so the markup CANNOT see
 *   the page's CSS custom properties. The `var(--illo-*)` fills we added would
 *   silently fall back to their light-mode values and dark mode would never
 *   engage. Inlining puts the SVG in the page's own DOM, where the vars resolve
 *   and `.dark` can flip them.
 *
 * This is a codegen step, not a runtime one — the output is committed.
 */
import { readFileSync, writeFileSync } from "node:fs";

const SRC = [
  ["asset-15.svg", "CharityMarketIllustration"],
  ["asset-16.svg", "ChecklistIllustration"],
  ["asset-17.svg", "HandoffIllustration"],
  ["asset-18.svg", "GivingIllustration"],
];

function toJsx(svg) {
  return (
    svg
      // strip the XML prolog / comments if any
      .replace(/<\?xml[^>]*\?>/g, "")
      .replace(/<!--[\s\S]*?-->/g, "")
      // SVG attrs -> JSX attrs
      .replace(/\b(stroke-width|stroke-linecap|stroke-linejoin|stroke-dasharray|stroke-miterlimit|fill-rule|clip-rule|stop-color|stop-opacity|clip-path)=/g, (m, a) =>
        a.replace(/-([a-z])/g, (_, c) => c.toUpperCase()) + "=",
      )
      .replace(/\bxmlns:xlink=/g, "xmlnsXlink=")
      .replace(/\bxlink:href=/g, "xlinkHref=")
      // the root <svg> gets sized by the caller
      .replace(/<svg[^>]*>/, '<svg viewBox="0 0 200 200" className={className} aria-hidden="true" focusable="false">')
      .trim()
  );
}

const parts = SRC.map(([file, name]) => {
  const svg = readFileSync(`public/assets/${file}`, "utf8");
  return `export function ${name}({ className }: { className?: string }) {
  return (
    ${toJsx(svg)}
  )
}`;
});

const out = `// GENERATED by scripts/build-illustrations.mjs — do not edit by hand.
//
// The four illustrations, inlined as React components so their \`var(--illo-*)\`
// fills can resolve against the page's CSS custom properties. Rendered via
// <img src="*.svg"> the SVG sits in an isolated document, the vars fall back to
// their light-mode defaults, and dark mode silently does nothing.
//
// Colour tokens live in v4-type.css (\`--illo-paper\`, \`--illo-light\`,
// \`--illo-mid\`, \`--illo-dark\`, \`--illo-ink\`) and flip under \`.dark\`.

${parts.join("\n\n")}
`;

writeFileSync("src/app/v4/Illustrations.tsx", out);
console.log(`wrote src/app/v4/Illustrations.tsx (${(out.length / 1024).toFixed(0)} KB)`);
